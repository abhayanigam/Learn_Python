import numpy as np
#Sorting an array.
arr = np.array([4,2,0,3,1])
print(np.sort(arr))

#Sorting of string.
arr_2 = np.array(['banana','cherry','apple'])
print(np.sort(arr_2))

#Sorting the boolean array.
arr_3 = np.array([True,False,True,False])
print(np.sort(arr_3))

#Sorting a 2D array.
arr_4 = np.array([[3,5,2,1,4],[9,5,7,6,8]])
print(np.sort(arr_4))